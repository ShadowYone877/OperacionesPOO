// Definimos la clase Resta
class Resta {
    private int num, num2; // Atributos privados

    public void setNum(int num) { this.num = num; } // Método para asignar num
    public void setNum2(int num2) { this.num2 = num2; } // Método para asignar num2
    public int calcular() { return num - num2; } // Método para calcular la resta
}
