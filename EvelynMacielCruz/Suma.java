// Clase que realiza la operación de suma
class Suma {
    private int num, num2; // Variables privadas para los números

    // Métodos para asignar valores a las variables
    public void setNum(int num) { this.num = num; }
    public void setNum2(int num2) { this.num2 = num2; }

    // Método para calcular la suma de los dos números
    public int calcular() { return num + num2; }
}
